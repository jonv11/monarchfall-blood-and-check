namespace MFBC.Core;

/// <summary>
/// Represents the side or faction of a piece.
/// </summary>
public enum Side
{
    /// <summary>
    /// The first player.
    /// </summary>
    White = 0,

    /// <summary>
    /// The second player.
    /// </summary>
    Black = 1,

    /// <summary>
    /// Neutral side for non-player entities.
    /// </summary>
    Neutral = 2
}
